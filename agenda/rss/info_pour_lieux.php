<h2 align="center">Infos pour les LIEUX qui veulent nous fournir leur contenu via flux RSS </h2>
<p>&nbsp;</p>
<p>Afin de lire un flux RSS en provenance d'un LIEU culturel,  on a besoin au minimum de certaines infos :</p>
<ul>
  <li><strong>title</strong></li>
  <li><strong>link</strong></li>
  <li><strong>description</strong></li>
</ul>
<p>&nbsp;</p>
<p>L'info de &quot;<strong>date</strong>&quot; est indispensable : on a besoin  au minimum d'1 date pour dire quel jour l'&eacute;v&eacute;nement se d&eacute;roulera. Mais si  l'&eacute;v&eacute;nement se r&eacute;p&egrave;te dans le temps, il est pr&eacute;f&eacute;rable de transmettre toutes les  dates, par exemple sous cette forme : </p>
<p>&nbsp;</p>


<p>&lt;openingdates&gt;</p>
<blockquote>
